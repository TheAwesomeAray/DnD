using System.Collections.Generic;
using System.Threading.Tasks;
using DnD.Characters.Core.Interfaces;
using Microsoft.EntityFrameworkCore;

namespace DnD.Characters.Infrastructure.Data
{
    public class Repository<T> : IAsyncRepository<T> where T : class
    {
        protected DnDContext _context;

        public Repository(DnDContext context)
        {
            _context = context;
        }

        public async Task<T> AddAsync(T entity)
        {
            _context.Set<T>().Add(entity);
            await _context.SaveChangesAsync();

            return entity;
        }

        public async Task DeleteAsync(T entity)
        {
            _context.Set<T>().Remove(entity);
            await _context.SaveChangesAsync();
        }

        public async Task<T> GetByIdAsync(int id)
        {
            return await _context.Set<T>().FindAsync(id);
        }

        public async Task<IReadOnlyList<T>> ListAllAsync()
        {
            return await _context.Set<T>().ToListAsync();
        }

        public async Task UpdateAsync(T entity)
        {
            _context.Entry(entity).State = EntityState.Modified;
            await _context.SaveChangesAsync();
        }
    }
}
